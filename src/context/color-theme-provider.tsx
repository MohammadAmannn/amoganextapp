export const DEFAULT_COLOR_THEME = 'zinc'

type ColorThemeProviderState = {
  colorTheme: string
  setColorTheme: (name: string) => void
  resetColorTheme: () => void
  currentTheme: ColorThemeDefinition
}

const ColorThemeContext = createContext<ColorThemeProviderState>({
  colorTheme: DEFAULT_COLOR_THEME,
  setColorTheme: () => null,
  resetColorTheme: () => null,
  currentTheme: colorThemes[0],
})

function applyColorTokens(themeName: string, isDark: boolean) {
  const theme = colorThemes.find((t) => t.name === themeName)
  if (!theme) return

  const root = document.documentElement

  // Clear previous theme overrides so stale vars don't leak between themes
  for (const prop of ALL_THEME_VARS) {
    root.style.removeProperty(prop)
  }

  const tokens = isDark ? theme.tokens.dark : theme.tokens.light
  for (const [prop, value] of Object.entries(tokens)) {
    root.style.setProperty(prop, value)
  }

  // ── Derive sidebar vars from theme so the full page reflects the theme ──
  // Only set sidebar vars if the theme didn't explicitly define them
  const bg = tokens['--background']
  const fg = tokens['--foreground']
  const primary = tokens['--primary']
  const primaryFg = tokens['--primary-foreground']
  const accent = tokens['--accent']
  const accentFg = tokens['--accent-foreground']
  const border = tokens['--border']
  const ring = tokens['--ring']

  if (bg && !tokens['--sidebar']) {
    // Sidebar background: slightly darker/different shade than the page bg
    // We use the card color if available, otherwise bg itself
    const sidebarBg = tokens['--card'] ?? bg
    root.style.setProperty('--sidebar', sidebarBg)
  }
  if (fg && !tokens['--sidebar-foreground']) {
    root.style.setProperty('--sidebar-foreground', fg)
  }
  if (primary && !tokens['--sidebar-primary']) {
    root.style.setProperty('--sidebar-primary', primary)
  }
  if (primaryFg && !tokens['--sidebar-primary-foreground']) {
    root.style.setProperty('--sidebar-primary-foreground', primaryFg)
  }
  if (accent && !tokens['--sidebar-accent']) {
    root.style.setProperty('--sidebar-accent', accent)
  }
  if (accentFg && !tokens['--sidebar-accent-foreground']) {
    root.style.setProperty('--sidebar-accent-foreground', accentFg)
  }
  if (border && !tokens['--sidebar-border']) {
    root.style.setProperty('--sidebar-border', border)
  }
  if (ring && !tokens['--sidebar-ring']) {
    root.style.setProperty('--sidebar-ring', ring)
  }
}

export function ColorThemeProvider({ children }: { children: React.ReactNode }) {
  const { resolvedTheme } = useTheme()
  const [colorTheme, _setColorTheme] = useState<string>(
    () => getCookie(COLOR_THEME_COOKIE) || DEFAULT_COLOR_THEME
  )

  const currentTheme = useMemo(
    () => colorThemes.find((t) => t.name === colorTheme) || colorThemes[0],
    [colorTheme]
  )

  useEffect(() => {
    applyColorTokens(colorTheme, resolvedTheme === 'dark')
  }, [colorTheme, resolvedTheme])

  const setColorTheme = useCallback(
    (name: string) => {
      setCookie(COLOR_THEME_COOKIE, name, COLOR_THEME_COOKIE_MAX_AGE)
      _setColorTheme(name)
    },
    []
  )

  const resetColorTheme = useCallback(() => {
    removeCookie(COLOR_THEME_COOKIE)
    _setColorTheme(DEFAULT_COLOR_THEME)
  }, [])

  const value = useMemo(
    () => ({
      colorTheme,
      setColorTheme,
      resetColorTheme,
      currentTheme,
    }),
    [colorTheme, setColorTheme, resetColorTheme, currentTheme]
  )

  return (
    <ColorThemeContext value={value}>
      {children}
    </ColorThemeContext>
  )
}

// eslint-disable-next-line react-refresh/only-export-components
export const useColorTheme = () => {
  const context = useContext(ColorThemeContext)
  if (!context)
    throw new Error('useColorTheme must be used within a ColorThemeProvider')
  return context
}
